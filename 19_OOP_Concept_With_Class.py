# OOP concept

# Class hocche factory ba blue print er moto, class er moddhe ja thakbe ta proyojon moto pore uesr kora jabe
class car:

    # variable
    model = "Honda"

    # Class er moddhe jokhn kono function thake tokhn take method ble
    def test(self):
        print("Nissan")


# car class er kono property use korar jonno sey class er akta object create korte hbe
# car class k car() object hisebe use kora hocche object k abar kono akta variable er moddhe rakha hochhe
A = car()
print(A.model)
print(A.test())

# Akadhik object create kora jai
B = car()
print(B.model)


